int		f_plus(int a, int b)
{
	return (a + b);
}

int		f_minus(int a, int b)
{
	return (a - b);
}

int		f_multiply(int a, int b)
{
	return (a * b);
}

int		f_divide(int a, int b)
{
	return (a / b);
}

int		f_mod(int a, int b)
{
	return (a % b);
}
