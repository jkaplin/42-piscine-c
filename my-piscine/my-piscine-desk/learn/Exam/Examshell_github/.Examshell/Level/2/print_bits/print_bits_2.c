#include <stdio.h>

void	print_bits(unsigned char octet)
{
	int		byte;

	byte = 128;
	while (byte > 0)
	{
		if (octet & byte != 0)
			write(1, "1", 1);
		else
			write(1, "0", 1);
		byte /= 2;
	}
}
